    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ControladorPresentacion ctrlP = ControladorPresentacion.getInstance();
        String titulo = jTextPane1.getText();
        String autor = jTextPane2.getText();
        String path = jTextPane3.getText();
        DocumentKey dk = null;
        try {
            dk = new DocumentKey (titulo, autor);
        } catch (Exception ex) {
            Logger.getLogger(ExportarDocumento.class.getName()).log(Level.SEVERE, null, ex);
        }
        ctrlP.exportarDocumento(path, dk, f);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

