    private void jButtonActionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonActionMouseClicked
        ControladorPresentacion cp = (new ControladorPresentacion()).getInstance();
        String expresion = this.jTextPane1.getText();
        if (expresion.isBlank()) {
            cp.error("Debes escribir alguna expresi贸n para crearla");
            this.jTextPane1.setBackground(Color.LIGHT_GRAY);
        }
        else {
            if (isCreate) {
                cp.nuevaExpresion(expresion);
            }
            else {
                cp.modificarExpresion(expresion, idxToUpdate);
            }
            cp.updatePrincipal();

            this.setVisible(false);
        }
        
    }//GEN-LAST:event_jButtonActionMouseClicked

