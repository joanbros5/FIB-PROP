    public void updateData() {
        DefaultListModel modelo = new DefaultListModel();
        
        ControladorPresentacion cp = ControladorPresentacion.getInstance();
        ArrayList<String> le = cp.getListaExpresiones();
        for (String e:
             le) {
             modelo.addElement(e);
        }
        this.jList1.setModel(modelo);
    }

