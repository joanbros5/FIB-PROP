    private void borrarActionClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionClicked
        int idxSelected = this.jList1.getSelectedIndex();
        if (idxSelected == -1) {
            ControladorPresentacion cp = (new ControladorPresentacion()).getInstance();
            cp.error("Debes seleccionar alguna expresión para borrarla");
        }
        else {
            System.out.println(idxSelected);

            ControladorPresentacion cp = (new ControladorPresentacion()).getInstance();
            cp.deleteExpresion(idxSelected);
            this.updateData();
        }
    }//GEN-LAST:event_borrarActionClicked

